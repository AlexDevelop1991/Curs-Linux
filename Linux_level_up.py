# sudo usermod -aG (name user) ---> Добавить пользователя в группу.
# sudo deluser (user name)(названия группы) ---> Удалить пользователя из группы.
# /ect/skel ---> Шаблон домашней директории.

"""Доступ и владения файлами/директориями"""

# sudo crown (владелец файла)(какой файл) ---> поменять владельца файла/директории.
# sudo chgrp (названия группы)(какой файл) ---> Поменять группу  файла/директории.
# chmod ---> изменить права доступа на файла/директории.
# chmod ugo+x (названия файла) ---> добавить X всем.
# chmod g-rm (названия файла) ---> убрать RW у группы.
# chmod o=rw (названия файла) ---> установить RW всем остальным.
# u = user,  g = group,  o = other,  a = ugo.
# r(read) = 4,  w(write) = 2,  x(execute) = 1.
# rwx = 7 = 4+2+1
# rw- = 6 = 4+2+0
# r-x = 5 = 4+0+1
# r-- = 4 = 4+0+0
# -wx = 3 = 0+2+1
# -w- = 2 = 0+2+0
# --x = 1 = 0+0+1
# --- = 0 = 0+0+0

