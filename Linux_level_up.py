"""Пользователи и группы"""

# sudo useradd -m (name user) ---> создать нового пользователя m - создаст домашнию директорию для пользователя.
# sudo passwd (name user) ---> устанавливаем пароль для пользователя.
# sudo userdel -r (user name) ---> Удаление пользователя r - удалит его домашнию директорию.
# sudo groupadd (названия группы) ---> Создаём группу.
